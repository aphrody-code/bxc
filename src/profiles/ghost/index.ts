/**
 * @module bxc/profiles/ghost
 *
 * `ghost` profile â€” Lightpanda-backed anti-detection automation for servers.
 *
 * Why Lightpanda :
 *   - 80 ms cold start vs 800 ms (Chromium) / 1500 ms (Camoufox)
 *   - 40 MB idle RSS vs 200-400 MB
 *   - No display required (true headless, no Xvfb)
 *   - Same V8 as Chrome â†’ all stealth patches written for Chromium apply
 *
 * Lightpanda alone, however, ships with `navigator.webdriver === true`
 * and an empty `navigator.plugins` array â€” instant tells. `ghost` solves
 * this by injecting a complete stealth patch suite via CDP
 * `Page.addScriptToEvaluateOnNewDocument` BEFORE the first navigation,
 * combined with a coherent fingerprint (UA, viewport, locale, timezone,
 * WebGL vendor/renderer) generated by `fingerprint.ts`.
 *
 * Coverage matrix vs the existing profiles :
 *
 *   - `static` / `fast` : minimal anti-detection, 0 ms / 80 ms cold start
 *   - `http`            : TLS-fingerprint only (curl-impersonate JA4)
 *   - `stealth`         : patchright Chromium, 800 ms cold, 200 MB RSS
 *   - `max`             : Camoufox FF + CapSolver, 1.5 s cold, 400 MB RSS
 *   - `ghost`           : Lightpanda + custom stealth, 80 ms cold, 40 MB RSS
 *
 * On a Linux VPS where neither patchright Chromium nor Camoufox can be
 * installed (no display, RAM-constrained), `ghost` is the only option
 * that beats CF Managed Challenge with a sub-100 ms cold start.
 *
 * Usage :
 *
 * ```ts
 * import { launchGhostBrowser } from "@aphrody-code/bxc/profiles/ghost";
 *
 * const ghost = await launchGhostBrowser({
 *   fingerprint: { os: "linux", browser: "chrome" },
 *   locale: "fr-FR",
 *   timezone: "Europe/Paris",
 *   cookies: "./cookies/private/challonge.json",
 * });
 *
 * await ghost.page.goto("https://challonge.com/fr/B_TS5");
 * console.log(await ghost.page.content());
 * await ghost.close();
 * ```
 */

