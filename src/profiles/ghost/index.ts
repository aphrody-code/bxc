// ---------------------------------------------------------------------------
// Options
// ---------------------------------------------------------------------------

export interface GhostOptions {
	/** Override fingerprint generation (OS / browser family / version / seed). */
	fingerprint?: FingerprintOptions;
	/** Locale used for `Accept-Language` and `navigator.languages`. */
	locale?: string;
	/** IANA timezone used for the rendering context. */
	timezone?: string;
	/** Pre-validated cookie jar â€” JSON path or `Cookie[]` array. */
	cookies?: string;
	/** Lightpanda binary override. */
	binaryPath?: string;
	/** Lightpanda log level. Default `"error"`. */
	logLevel?: "debug" | "info" | "warn" | "error";
	/** Verbose logger (defaults to silent). */
	log?: (msg: string) => void;
	/** Disable canvas / WebGL / chrome.runtime patches (useful when an upstream
	 *  framework like patchright already injects them). */
	skipStealthInject?: boolean;
}

export interface GhostBrowser {
	/** Page handle (CDP-connected via SocketPairTransport). */
	page: Page;
	/** Negotiated coherent fingerprint. */
	fingerprint: BrowserFingerprint;
	/** Closes the page + the underlying Lightpanda subprocess. */
	close(): Promise<void>;
	[Symbol.asyncDispose](): Promise<void>;
}

// ---------------------------------------------------------------------------
// Launch
// ---------------------------------------------------------------------------

export async function launchGhostBrowser(
	options: GhostOptions = {},
): Promise<GhostBrowser> {
	const log = options.log ?? (() => {});

	// 1. Coherent fingerprint generation (defaults : Linux + Chrome 131)
	const fingerprint = generateFingerprint({
		os: options.fingerprint?.os ?? "linux",
		browser: options.fingerprint?.browser ?? "chrome",
		version: options.fingerprint?.version,
		seed: options.fingerprint?.seed,
	});
	log(
		`[ghost] ua="${fingerprint.userAgent.slice(0, 60)}â€¦" viewport=${fingerprint.screen.width}x${fingerprint.screen.height} platform=${fingerprint.platform}`,
	);

	// 2. Open Lightpanda (profile=fast under the hood)
	const page = (await Browser.newPage({
		profile: "fast",
		userAgent: fingerprint.userAgent,
		viewport: {
			width: fingerprint.screen.width,
			height: fingerprint.screen.height,
		},
		cookies: options.cookies,
		spawnOpts: {
			logLevel: options.logLevel ?? "error",
			readyTimeoutMs: 10_000,
			binaryPath: options.binaryPath,
		},
	})) as Page;

	// 3. Inject the stealth patch suite â€” runs before every navigation.
	// `Page.addScriptToEvaluateOnNewDocument` is the canonical hook for
	// injecting JS that must execute prior to any site script.
	if (!options.skipStealthInject) {
		const script = buildStealthScript({
			userAgent: fingerprint.userAgent,
			platform: fingerprint.platform,
			languages: options.locale
				? [options.locale, ...fingerprint.languages]
				: fingerprint.languages,
			hardwareConcurrency: fingerprint.hardwareConcurrency,
			deviceMemory: fingerprint.deviceMemory,
			webglVendor: fingerprint.webglVendor,
			webglRenderer: fingerprint.webglRenderer,
			devicePixelRatio: fingerprint.devicePixelRatio,
			screen: fingerprint.screen,
		});

		try {
			await sendCdp(page, "Page.addScriptToEvaluateOnNewDocument", {
				source: script,
			});
			log(`[ghost] stealth script injected (${script.length} bytes)`);
		} catch (err) {
			const msg = err instanceof Error ? err.message : String(err);
			log(
				`[ghost] stealth inject failed (${msg.slice(0, 80)}); continuing without`,
			);
		}
	}

	// 4. Apply Emulation overrides â€” locale + timezone affect Accept-Language
	// + Date.prototype.toLocaleString + Intl.DateTimeFormat. Without these
	// the geo IP / locale mismatch is itself an anti-bot signal.
	if (options.locale) {
		await sendCdp(page, "Emulation.setLocaleOverride", {
			locale: options.locale,
		}).catch(() => {});
		await sendCdp(page, "Network.setExtraHTTPHeaders", {
			headers: { "Accept-Language": `${options.locale},en-US;q=0.9,en;q=0.8` },
		}).catch(() => {});
	}
	if (options.timezone) {
		await sendCdp(page, "Emulation.setTimezoneOverride", {
			timezoneId: options.timezone,
		}).catch(() => {});
	}

	// 5. Override User Agent since Page.create currently ignores opts.userAgent
	await sendCdp(page, "Emulation.setUserAgentOverride", {
		userAgent: fingerprint.userAgent,
		acceptLanguage: fingerprint.acceptLanguage,
		platform: fingerprint.platform,
	}).catch(() => {});

	const close = async () => {
		try {
			await page.close();
		} catch {
			// ignore
		}
		await Browser.close().catch(() => {});
	};

	return {
		page,
		fingerprint,
		close,
		[Symbol.asyncDispose]: close,
	};
}

// ---------------------------------------------------------------------------
// Internals
// ---------------------------------------------------------------------------

/** Bxc `Page` exposes `_internalTransport`/`_internalSessionId` for direct CDP calls. */
async function sendCdp(
	page: Page,
	method: string,
	params: Record<string, unknown>,
): Promise<unknown> {
	const transport = (
		page as unknown as {
			_internalTransport: {
				send: (raw: string) => void;
				onmessage?: (raw: string) => void;
			};
		}
	)._internalTransport;
	const sessionId = (page as unknown as { _internalSessionId: string })
		._internalSessionId;
	const id = Math.floor(Math.random() * 1_000_000_000);
	return new Promise<unknown>((resolve, reject) => {
		const prev = transport.onmessage;
		const timeout = setTimeout(() => {
			transport.onmessage = prev;
			reject(new Error(`CDP ${method} timed out`));
		}, 10_000);
		transport.onmessage = (raw: string) => {
			prev?.call(transport, raw);
			let msg: { id?: number; result?: unknown; error?: { message: string } };
			try {
				msg = JSON.parse(raw);
			} catch {
				return;
			}
			if (msg.id !== id) return;
			clearTimeout(timeout);
			transport.onmessage = prev;
			if (msg.error)
				reject(new Error(`CDP ${method} failed: ${msg.error.message}`));
			else resolve(msg.result);
		};
		transport.send(JSON.stringify({ id, method, params, sessionId }));
	});
}

